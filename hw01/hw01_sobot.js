function fullDecks (cards){
    const cardsCount = {};

    // create object with unique cards as keys, and number of repetitions as values
    cards.forEach(element => {
        cardsCount[element] = (cardsCount[element] || 0) + 1;
    });

    // get number of repetitions from object
    const numOfRepeating = Object.values(cardsCount);

    if (numOfRepeating.length == 52){
        // number of possible decks is the same as the smallest number of repetition from any card
        return numOfDecks = Math.min(...numOfRepeating)
    } else {
        return numOfDecks = 0;
    }
}

